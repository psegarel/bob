        <div id="readme" class="clearfix announce instapaper_body " data-path="/">
          <span class="name"><span class="mini-icon mini-icon-readme"></span> README</span><div class="plain"><pre>This library provides PHP support for the Twitter REST API 1.1.

It is used by Drupal's Twitter module <a href="http://drupal.org/project/twitter">http://drupal.org/project/twitter</a>. OAuth
authentication is made thanks to <a href="http://drupal.org/project/twitter">http://drupal.org/project/twitter</a>

The library has been moved away from the module so it can be used by other
Twitter related modules, and even other PHP projects which do not use Drupal.

If you want to use it for your own PHP project, just change
variable_get('twitter_api', TWITTER_API) by <a href="http://api.twitter.com">http://api.twitter.com</a> and
the drupal_http_request by any request implementation (you could use CURL, for
example).
</pre></div>
